     public ArrayList<Product1> productList()
    {
        ArrayList<Product1> productList = new ArrayList<>();
        try
            {
              Class.forName("org.postgresql.Driver");
             Connection con=DriverManager.getConnection("jdbc:postgresql://localhost:5432/army","postgres","postgres");  
                 Statement st = con.createStatement();
                String sql = "select * from product";
                ResultSet rs = st.executeQuery(sql);
                Product1 product;
                while(rs.next())
                {
                  product = new Product1(rs.getInt(1),rs.getInt(2),rs.getString(3),rs.getInt(4),rs.getInt(5));
                  productList.add(product);
                }
              }catch(Exception e)
                {
                }
       return productList;
    }

    public void showProduct()
    {
      ArrayList<Product1> list = productList();
      DefaultTableModel model =(DefaultTableModel)displayproduct.getModel();
      Object[] row = new Object[5];
      for(int i=0;i<list.size();i++)
      {
        row[0] =list.get(i).getId();
        row[1] =list.get(i).getCid();
        row[2] =list.get(i).getName();
        row[3] =list.get(i).getPrice();
        row[4] =list.get(i).getQuantity();
        model.addRow(row);
      }
    }
